let M = 1;

switch (true) {
    case (M <= 10):
        console.log("E");
        break;

    case (M <= 20):
        console.log("D");
        break;

    case (M <= 30):
        console.log("C");
        break;

    case (M <= 40):
        console.log("B");
        break;

    case (M <= 50):
        console.log("A");
        break;

    default:
        console.log("Invalid Marks");
}